// User function template for C++
class Solution {
  public:
    int print2largest(vector<int> &arr) {
        set<int> s(arr.begin(), arr.end());
        
        int n = s.size();
        if(n < 2)
        {
            return -1;
        }
        auto it = s.rbegin();
        it++;
        return *it;
    }
};

